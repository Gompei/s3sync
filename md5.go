package s3sync

import (
	"crypto/md5"
	"fmt"
	"io"
	"os"
)

// getMd5Hash return the MD5 hash value of the specified file
func getMd5Hash(filePath string) (string, error) {
	file, err := os.Open(filePath)
	if err != nil {
		return "", err
	}
	defer file.Close()

	hash := md5.New()
	if _, err := io.Copy(hash, file); err != nil {
		return "", err
	}

	return fmt.Sprintf("%x", hash.Sum(nil)), nil
}
